# Percabangan if dan else

nilai = int(input("Masukkan nilai : "))

if nilai >= 70 and nilai <= 100:
    print('Lulus')
else:
    print('Tidak Lulus')
 
# Jika kondisi yang diberikan bernilai benar (True), maka blok kode tertentu akan dijalankan. 
# Jika kondisi tersebut bernilai salah (False), maka blok kode lainnya (yang ada di bagian else) akan dijalankan.